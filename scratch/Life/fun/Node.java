public class Node {
	
	int posX,posY;
	float colour;

	
	public Node(int posX, int posY, float colour){
		this.posX = posX;
		this.posX = posX;
		this.colour = colour;
		
		
		
	}


	public int getPosX() {
		return posX;
	}


	public void setPosX(int posX) {
		this.posX = posX;
	}


	public int getPosY() {
		return posY;
	}


	public void setPosY(int posY) {
		this.posY = posY;
	}


	public float getColour() {
		return colour;
	}


	public void setColour(float colour) {
		this.colour = colour;
	}
}
